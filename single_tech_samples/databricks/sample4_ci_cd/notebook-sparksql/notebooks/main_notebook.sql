DROP TABLE IF EXISTS  FIRST_YEAR_POPULATION;
CREATE TABLE IF NOT EXISTS FIRST_YEAR_POPULATION AS (
  SELECT date as YEAR, value as PUPULATION FROM US_POPULATION where date = (select MIN(date) from US_POPULATION))

